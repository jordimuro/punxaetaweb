export type RouteTemplateSeed = {
  order: number;
  name: string;
  town: string;
  kms: number;
  elevationGain: number;
  breakfastPlace: string;
  distanceToBreakfast: number;
  elevationToBreakfast: number;
  notes: string;
  meetingPoint: string;
  slug: string;
  summary: string;
};

function normalizeText(value: string) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-zA-Z0-9]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

function cleanNameForTown(name: string) {
  return name
    .replace(/\([^)]*\)/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function extractTown(name: string) {
  const cleaned = cleanNameForTown(name);
  const pieces = cleaned.split(/\s+(?:per|pel|via|i|o)\s+/i);
  const primary = pieces[0]?.trim() || cleaned;
  return primary || "Sense poblacio";
}

function extractBreakfastPlace(notes: string) {
  const match = notes.match(/([A-Za-z0-9À-ÿ'".\- ]{2,}?)\s*\(Esmorzar\)/i);
  if (!match) {
    return "Per determinar";
  }
  return match[1]
    .replace(/[-–]+$/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function extractBreakfastStats(notes: string, totalKms: number, totalElevation: number) {
  const aroundEsmorzar = notes.match(
    /(\d{2,3})\s*km[^↑\d]{0,20}↑\s*(\d{2,4})\s*m[^()]{0,40}\(Esmorzar\)|\(Esmorzar\)[^0-9]{0,20}(\d{2,3})\s*km[^↑\d]{0,20}↑\s*(\d{2,4})\s*m/i,
  );

  if (aroundEsmorzar) {
    const km = Number(aroundEsmorzar[1] || aroundEsmorzar[3]);
    const elev = Number(aroundEsmorzar[2] || aroundEsmorzar[4]);
    if (Number.isFinite(km) && Number.isFinite(elev)) {
      return { distanceToBreakfast: km, elevationToBreakfast: elev };
    }
  }

  return {
    distanceToBreakfast: Math.max(1, Math.round(totalKms / 2)),
    elevationToBreakfast: Math.max(1, Math.round(totalElevation / 2)),
  };
}

function extractMeetingPoint(notes: string) {
  const first = notes.split(/[–-]/)[0]?.trim() ?? "";
  return first || "Muro";
}

function buildSummary(notes: string) {
  const normalized = notes.replace(/\s+/g, " ").trim();
  if (normalized.length <= 180) {
    return normalized;
  }
  return `${normalized.slice(0, 177).trimEnd()}...`;
}

export function parseRouteTemplatesCatalog(text: string): RouteTemplateSeed[] {
  const normalized = text.replace(/\r\n/g, "\n");
  const titleRegex = /^\s*(\d+)\.\s+(.+?)\s*[-–]\s*(\d+)\s*Km?\s*↑\s*(\d+)\s*m\s*$/gim;
  const matches = [...normalized.matchAll(titleRegex)];

  const seeds: RouteTemplateSeed[] = [];
  for (let index = 0; index < matches.length; index += 1) {
    const match = matches[index];
    const next = matches[index + 1];
    const order = Number(match[1]);
    const rawName = match[2].replace(/\s+/g, " ").trim();
    const kms = Number(match[3]);
    const elevationGain = Number(match[4]);
    const bodyStart = (match.index ?? 0) + match[0].length;
    const bodyEnd = next?.index ?? normalized.length;
    const body = normalized.slice(bodyStart, bodyEnd).trim();
    const notes = body.replace(/^Recorregut:\s*/i, "").replace(/\s+/g, " ").trim();
    const town = extractTown(rawName);
    const breakfastPlace = extractBreakfastPlace(notes);
    const { distanceToBreakfast, elevationToBreakfast } = extractBreakfastStats(
      notes,
      kms,
      elevationGain,
    );
    const meetingPoint = extractMeetingPoint(notes);
    const slug = `seed-${String(order).padStart(2, "0")}-${normalizeText(rawName).slice(0, 55)}`;

    seeds.push({
      order,
      name: rawName,
      town,
      kms,
      elevationGain,
      breakfastPlace,
      distanceToBreakfast,
      elevationToBreakfast,
      notes,
      meetingPoint,
      slug,
      summary: buildSummary(notes),
    });
  }

  return seeds.sort((a, b) => a.order - b.order);
}

export const ROUTE_TEMPLATES_CATALOG = parseRouteTemplatesCatalog(ROUTE_TEMPLATES_CATALOG_TEXT);
